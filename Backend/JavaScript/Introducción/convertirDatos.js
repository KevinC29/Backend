//Conversion de datos
/*
* a String => String(), toString() 
* a Int => Number(), parseInt()
* a Float => Number(), parseFloat()
*/


//Int to String
let dato = 15;
console.log(typeof(dato));

//dato = String(dato);
dato = toString(dato);
console.log(typeof(dato));

//String to Int
dato = '45';
console.log(typeof(dato));

//dato = Number(dato);
dato = parseInt(dato);
console.log(typeof(dato));

//String to Float
dato = '4.5';
console.log(typeof(dato));

//dato = Number(dato);
dato = parseFloat(dato);
console.log(typeof(dato));